# Basic usage of print()
print("Hello, world")
print("Welcome to Python programming")

# Use print() with variables
name = "Aisha"
age = 25
print("name: ", name)
print("Age: ", age)

# Using f-string with print()
name = "Bola"
score = 92
print(f"{name} scored {score} in the exam")

# using concatenation
first_name = "Ada"
last_name = "Lovelace"
print("Full name: " + first_name + " " + last_name)

# Comma vs concatenation
print("Hello", "world!")
print("Hello" + " " + "world!")

